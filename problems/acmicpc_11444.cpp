#include <stdio.h>
using ll = long long;
void mul(ll const A[4], ll const B[4], ll C[4]) {
	C[0] = ((A[0] * B[0]) % 1000000007 + (A[1] * B[2]) % 1000000007) % 1000000007;
	C[1] = ((A[0] * B[1]) % 1000000007 + (A[1] * B[3]) % 1000000007) % 1000000007;
	C[2] = ((A[2] * B[0]) % 1000000007 + (A[3] * B[2]) % 1000000007) % 1000000007;
	C[3] = ((A[2] * B[1]) % 1000000007 + (A[3] * B[3]) % 1000000007) % 1000000007;
}
void f(ll A[4], ll n) {
	if(n == 0) {
		A[0] = A[3] = 1;
		A[1] = A[2] = 0;
		return;
	}
	if(n == 1) return;
	ll B[4] = {A[0], A[1], A[2], A[3]};
	f(B, n / 2);
	ll C[4] = {A[0], A[1], A[2], A[3]};
	f(C, n % 2);
	ll D[4];
	mul(B, B, D);
	mul(D, C, A);
}
